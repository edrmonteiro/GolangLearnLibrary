package main

import "fmt"

func main() {
	var a int = 24

	if a > 10 {
		fmt.Println("a is bigger than 10")
	} else {
		fmt.Println("a is bigger ou equal to 10")
	}

	if b := 11; b > 10 {
		fmt.Println("b is bigger than 10")
	} else {
		fmt.Println("b is bigger ou equal to 10")
	}

}
